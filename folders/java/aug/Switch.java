class Switch {
	
	public static void main(String aa[]) {
	
		double number = 12.0;
		
		switch(number) {
			
			case 1.0:
				System.out.println("The number is 1");
				
				break;
			case 10.2: 
				System.out.println("The number is 10");
				break;
			case 12.4:
				System.out.println("The number is 12");
				break;
			default:
				System.out.println("None");
		}
	}


}


