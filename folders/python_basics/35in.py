#in keyword used to check whether required elemennt is in the list or string or sets

#strings
print("s" in "dinesh")
print("i" in "bhanu")

#lists
d=[1,2,3,4,5]
print(4 in d)
print(6 in d)

#sets
g={"a","b","c","d","e","f"}    #for strings in sets "" are needed 
print("a" in g)
print("h" in g)

#numbersets
h={1,2,3,4,5,4} #for numbers in sets no need of ""
print(4 in h)
print(h)
